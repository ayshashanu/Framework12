package day17;

public class SwitchCase {
    public static void main(String[] args) {
        String days="tuesday";
        switch(days){
            case "sunday":
                System.out.println("Oh ! its holiday");
            case "monday":
                System.out.println("Oh ! its working day");
            case "tuesday":
                System.out.println("Oh ! its second working day");
        }
    }
}
