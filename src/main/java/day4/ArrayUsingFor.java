package day4;

public class ArrayUsingFor {
    public static void main(String[] args) {

        String arr[]=new String[5];
        arr[0]="kitkat";
        arr[1]="diary milk";
        arr[2]="munch";
        arr[3]="five star";
        arr[4]="perk";
        for(int i=0;i< arr.length;i++)
        {
            System.out.println(arr[i]);
        }

    }
}
